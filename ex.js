let teste =parseInt(prompt("Digite uma opção"));
switch(teste){
    case 1:
        document.write("Primeira opção")
        break;
    case 2:
        document.write("Segunda opção")
        break;
    case 3:
        document.write("terceira opção")
        break;
    default:
        document.write("Caso não seja a primeira e nem a segunda opção")
        break;
}